#include <iostream>
using namespace std;
int main () {
	// Declare variables:
	int a = 5, b = 10, c = 1;
	cout << "a = " << a << ", b = " << b << ", c = " << c << endl;
	// Arithmetic
	cout << "a + b = " << a + b << endl;
	cout << "b - a = " << b - a << endl;
	cout << "a * b = " << a * b << endl;
	cout << "b / a = " << b / a << endl;
	cout << "b % a = " << b % a << endl;
	cout << "a / 2 = " << a / 2 << endl;
	cout << "a % 2 = " << a % 2 << endl;
	cout << "++c = " << ++c << endl;
	cout << "--c = " << --c << endl;
	// End program:
	return 0;
}